package com.callor.hello;

public class Class04 {
	
	public static void main(String[] args) {
		
		int num1 = 33;
		int num2 = 5;
		
		// num1 과 num2 의 4칙연산을 수행하여
		// 각각 변수에 저장하고
		// 4칙연산 결과를 출력
		int sum = num1 + num2;
		int minus = num1 - num2;
		int mul = num1 * num2;
		int div = num1 / num2;
		System.out.println(sum);
		System.out.println(minus);
		System.out.println(mul);
		System.out.println(div);
				
		
		
		
	}

}
