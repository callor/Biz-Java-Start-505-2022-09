package com.callor.hello;

public class Var_02 {
	public static void main(String[] args) {
		
		int num1;
		int num2;
		
		num1 = 30;
		num2 = 40;
		
		/*
		 * 키워드가 있는 명령문에서
		 * EQ(=) 기호가 한번 나오면
		 * 왼쪽을 먼저 바라본다
		 */
		int num3 = 30;
		int num4 = 40;
		
		/*
		 * 키워드가 없는 명령문에서
		 * EQ(=) 기호가 한번 나오면
		 * 오른쪽을 먼저 바라본다
		 */
		num3 = 30 + 40;
		
		/*
		 * 1. 변수는 사용(값 저장)하기 전에 반드시
		 * 		선언절차가 필요하다
		 * 2. 한번 선언된 변수는 같은 이름으로
		 * 		중복하여 선언할 수 없다
		 * 3. 한번 선언된 변수에
		 * 		임의 값을 저장,할당 하면
		 * 		원래 저장된 값은 무조건 clear 된다
		 * 
		 */
		// num5 = 100;
		
		
		
	}

}
