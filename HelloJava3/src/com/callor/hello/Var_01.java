		// num2 라는 정수형 변수 선언
		int num2;
		
		// num3 라는 실수형 변수 선언
		float num3;
		// num4 라는 실수형 변수 선언
		float num4;
		
		// 코드의 위에서 선언된 num1 변수는
		// 또다시 선언할수 없다
