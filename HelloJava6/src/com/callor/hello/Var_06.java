package com.callor.hello;

public class Var_06 {
	public static void main(String[] args) {
		int num1 = 56;
		if(num1 == 0) {
			System.out.println("num1 은 0");
		}
		if(num1 > 33) {
			System.out.println(" num1 의 값은 33보다 크다");
		}
		if(num1 < 100) {
			System.out.println(" num1 의 값은 100보다 작다");
		}
		if(num1 > 56) {
			System.out.println(" num1 의 값은 56보다 크다");
		}
		if(num1 >= 56) {
			System.out.println(
					" num1 의 값은 56보다 크거나 같다");
		}
	

	}

}
