
// 여기는 자바 코드가 시작되는 곳이다
public class HelloClass {
	
	public static void main(String[] args) {
		
		// 여기는 숫자 30과 40의 덧셈을 계산하여
		// Console 에 출력하는 명령문이다
		System.out.println(30 + 40);

		/*
		 * 여기는 숫자 30과 40을 곱셈하여
		 * Console 에 출력하는 명령문이다
		 * 
		 *  내가 작성한 코드이다
		 *  Comment, 주석, 설명문을 작성하는 곳
		 *  Comment 는 java 컴파일러가 무시하는 곳이다
		 *  코드를 작성한 사람이 코드에 대한 설명등을
		 *  입력하여 시간이 흐른뒤에
		 *  코드를 쉽게 이해할수 있도록 하기 위함이다
		 */
		System.out.println(30 * 40);
	}
}
