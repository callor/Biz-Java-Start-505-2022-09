
public class HelloPrint {
	
	public static void main(String[] args) {
		System.out.println(30 + 40); // 덧셈
		System.out.println(40 - 30); // 뻴셈
		System.out.println(30 * 40); // 곱셈
		System.out.println(40 / 30); // 나눗셈, 몫
		System.out.println(40 % 5); // 나머지
		System.out.println(55 % 2); // 나머지
		System.out.println(40 % 2); // 나머지
	}

}
