package com.callor.hello;

public class Var_01 {

	/**
	 * main()
	 * 진입점, 시작점 method 라고 한다
	 * 진입점, 시작점 method 가 없으면
	 * 프로젝트를 완성하고 컴파일을 하여도 
	 * run(실행)이 되지 않는다
	 */
	public static void main(String[] args) {
		
		int num1 = 0; // 정수값을 저장하기 위한 변수
		float num2 = 0.0F; // 실수값을 저장하기 위한 변수
		
		// int 형 보다 더 큰 정수값을
		// 저장하는 변수 선언
		long num3 = 0L;  
		
		// float 보다 더 큰 실수값을 저장하는 변수 선언
		double num4 = 0.0;
		
		boolean bYes = true; // 또는 false
		
		/*
		 * 문자들의 집합을 문자열 이라고 하고
		 * 큰따옴표("")로 묶인 문자들의 집합
		 * 문자열을 저장하기 위한 변수 선언은
		 * String 이라는 키워드를 사용한다
		 */
		String str = "대한민국";
		str = "30 + 40 = " + (30 + 40);
		System.out.println(str);
	
				
		
		
		
	}
	
}
