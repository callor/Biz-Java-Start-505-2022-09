package com.callor.hello;

public class Var_03 {

	public static void main(String[] args) {
		
		int num1 = 0;
		
		num1 = num1 + 1;
		num1 += 1;
		num1 ++ ;
		++ num1;
		
		num1 = num1 - 1; 
		num1 -= 1;
		num1 --;
		-- num1;
		
		// 위에서 실행된 코드에서 
		// num1 에 어떤 값을 세팅했던지 상관없이
		// 다시 3 을 저장하라
		num1 = 3;
		num1 = num1 * 1;
		num1 *= 1;
		
		// 덧셈 num1 ++, 뺄셈 -- 이 외에는 안된다
		// num1 **;
		
		
		
		
	}
	
}
